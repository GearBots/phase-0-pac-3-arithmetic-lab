function add(a, b){ 
    return (a + b);
};
function increment(n){
    n += 1;
    return n;
}
function subtract(a, b){
    
    return a - b;
};
function decrement(n){
    n -= 1;
    return n;
};
function multiply(a, b){
    return a * b;
};
function makeInt(n){
    return parseInt(n,10);
};
function divide(a, b){
    return a / b;
}
function preserveDecimal(n){
    return parseFloat(n);
};